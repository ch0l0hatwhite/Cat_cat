def Tablero(tablero):
    print("-------------")
    for i in range(3):
        print("|", end=" ")
        for j in range(3):
            print(tablero[i][j], "|", end=" ")
        print("\n-------------")

def ganador(tablero, jugador):
    for i in range(3):
        if tablero[i][0] == tablero[i][1] == tablero[i][2] == jugador:
            return True
        if tablero[0][i] == tablero[1][i] == tablero[2][i] == jugador:
            return True
    if tablero[0][0] == tablero[1][1] == tablero[2][2] == jugador:
        return True
    if tablero[0][2] == tablero[1][1] == tablero[2][0] == jugador:
        return True
    return False

def gato():
    print("Gato gato #")

    tablero = [[" ", " ", " "],
               [" ", " ", " "],
               [" ", " ", " "]]

    jugador_actual = "X"
    turno = 1
    juego_terminado = False

    while not juego_terminado:
        Tablero(tablero)

        print(f"\n es turno {turno}: Jugador {jugador_actual}")
        fila = int(input("Fila : "))
        columna = int(input("Columna : "))

        if tablero[fila][columna] == " ":
            tablero[fila][columna] = jugador_actual
            if ganador(tablero, jugador_actual):
                Tablero(tablero)
                print(f"\nEl Ganador es : {jugador_actual}")
                juego_terminado = True
            elif turno == 9:
                Tablero(tablero)
                print("\nEmpatado")
                juego_terminado = True
            else:
                jugador_actual = "O" if jugador_actual == "X" else "X"
                turno += 1
        else:
            print("Posici√≥n ocupada, intenta otra casilla")


